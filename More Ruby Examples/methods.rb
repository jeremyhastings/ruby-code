def multiply(first_num, second_num)
  first_num.to_f * second_num.to_f
end
def divide(first_num, second_num)
  first_num.to_f / second_num.to_f
end
def add(first_num, second_num)
  first_num.to_f + second_num.to_f
end
def subtract(first_num, second_num)
  first_num.to_f - second_num.to_f
end
def modulus(first_num, second_num)
  first_num.to_f % second_num.to_f
end

puts multiply(5, 78)
puts divide(5, 78)
puts add(5, 78)
puts subtract(5, 78)
puts modulus(5, 78)

